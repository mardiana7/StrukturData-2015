
public class Vector3D
{
    public double x, y, z;
   

    /**
     * Constructor for objects of class Vektor3D
     */
    public Vector3D(double x, double y, double z)
    {
        this. x=x;
        this. y=y;
        this. z=z;
    }
   public double getX () {
       return x;
    }
    public double getY () {
       return y;
    }
    public double getZ () {
       return z;
    }
    
    
      
     
}
